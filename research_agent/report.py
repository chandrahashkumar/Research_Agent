# Report generator
# ---------------------------------------------------------------------------

class ReportGenerator:
    """
    Orchestrates IBM Granite calls to produce a full research report
    from a list of PaperSummary objects.
    """

    def __init__(self, llm: "GraniteClient", citation_mgr: "CitationManager") -> None:
        self._llm = llm
        self._citations = citation_mgr
        self._last_partial_report: "ResearchReport | None" = None

    def generate(
        self,
        topic: str,
        summaries: list["PaperSummary"],
    ) -> ResearchReport:
        """
        Build a complete ResearchReport for *topic* using *summaries*.

        Steps:
          1. Draft Introduction via Granite
          2. Draft Literature Review via Granite
          3. Draft Methodology Overview via Granite
          4. Aggregate Key Findings
          5. Suggest Hypotheses via Granite
          6. Draft Conclusion via Granite
          7. Format references

        If the IBM WML instance is inactive or any LLM call fails, each section
        falls back to a plain-text summary assembled from the paper abstracts so
        a complete (partial) report is always saved.
        """
        from research_agent.llm import _WatsonxError  # local import avoids circular

        report = ResearchReport(topic)
        papers_dicts = [
            {
                "title": s.title,
                "authors": s.authors,
                "year": s.year,
                "abstract": s.abstract,
            }
            for s in summaries
        ]
        summary_texts = [s.summary for s in summaries if s.summary]

        # Re-raise on the first LLM call so the error message surfaces cleanly;
        # subsequent sections fall back gracefully.
        llm_error: _WatsonxError | None = None

        def _section(name: str, fn, *args) -> str:
            nonlocal llm_error
            if llm_error is not None:
                return _fallback_section(name, summaries)
            try:
                return fn(*args)
            except _WatsonxError as exc:
                llm_error = exc
                return _fallback_section(name, summaries)

        report.introduction = _section(
            "Introduction", self._llm.generate_report_section, "Introduction", papers_dicts
        )
        report.literature_review = _section(
            "Literature Review", self._llm.generate_report_section, "Literature Review", papers_dicts
        )
        report.methodology_overview = _section(
            "Methodology Overview", self._llm.generate_report_section, "Methodology Overview", papers_dicts
        )
        report.key_findings = _section(
            "Key Findings", self._llm.generate_report_section, "Key Findings and Discussion", papers_dicts
        )
        report.hypotheses = _section(
            "Hypotheses", self._llm.suggest_hypotheses, topic, summary_texts
        )
        report.conclusion = _section(
            "Conclusion", self._llm.generate_report_section, "Conclusion", papers_dicts
        )

        # Register all papers and format reference list
        for s in summaries:
            self._citations.add_from_summary(s)
        report.references = self._citations.format_numbered_list()

        self._last_partial_report = report

        if llm_error is not None:
            raise llm_error

        return report

    # ------------------------------------------------------------------
    # Save helpers
    # ------------------------------------------------------------------

    def save_markdown(self, report: ResearchReport, output_dir: Path) -> Path:
        slug = _slugify(report.topic)
        path = output_dir / f"{slug}_report.md"
        path.write_text(report.to_markdown(), encoding="utf-8")
        return path

    def save_text(self, report: ResearchReport, output_dir: Path) -> Path:
        slug = _slugify(report.topic)
        path = output_dir / f"{slug}_report.txt"
        path.write_text(report.to_text(), encoding="utf-8")
        return path

    def save_pdf(self, report: ResearchReport, output_dir: Path) -> Path:
        slug = _slugify(report.topic)
        path = output_dir / f"{slug}_report.pdf"

        pdf = _ReportPDF(orientation="P", unit="mm", format="A4")
        pdf.set_auto_page_break(auto=True, margin=15)
        pdf.add_page()

        # Cover
        pdf.set_font("Helvetica", "B", 20)
        pdf.set_text_color(*_ReportPDF.TITLE_COLOR)
        pdf.multi_cell(0, 10, f"Research Report\n{report.topic}", align="C")
        pdf.ln(4)
        pdf.set_font("Helvetica", "I", 10)
        pdf.set_text_color(100, 100, 100)
        pdf.cell(0, 6, f"Generated: {report.generated_at}  |  IBM Granite (watsonx.ai)", align="C", ln=True)
        pdf.ln(6)

        sections = [
            ("1. Introduction", report.introduction),
            ("2. Literature Review", report.literature_review),
            ("3. Methodology Overview", report.methodology_overview),
            ("4. Key Findings", report.key_findings),
            ("5. Suggested Hypotheses", report.hypotheses),
            ("6. Conclusion", report.conclusion),
            ("References", report.references),
        ]
        for title, content in sections:
            pdf.chapter_title(title)
            pdf.body_text(content or "No content generated.")

        pdf.output(str(path))
        return path


# ---------------------------------------------------------------------------
# Utility
# ---------------------------------------------------------------------------

def _slugify(text: str) -> str:
    import re
    text = text.lower().strip()
    text = re.sub(r"[^\w\s-]", "", text)
    text = re.sub(r"[\s_-]+", "_", text)
    return text[:60]


def _fallback_section(section_name: str, summaries: list) -> str:
    """Build a plain-text section from paper abstracts when the LLM is unavailable."""
    lines = [f"[Auto-generated from paper abstracts — LLM unavailable]\n"]
    for s in summaries[:8]:
        title = getattr(s, "title", "Unknown")
        year = getattr(s, "year", "")
        abstract = getattr(s, "abstract", "") or getattr(s, "summary", "")
        lines.append(f"• {title} ({year}): {abstract[:300]}")
    return "\n".join(lines)
