"""
Report generator.

Produces a structured research report as:
  - Plain text  (.txt)
  - Markdown    (.md)
  - PDF         (.pdf) via fpdf2
"""

from __future__ import annotations

import textwrap
from datetime import datetime
from pathlib import Path
from typing import TYPE_CHECKING

from fpdf import FPDF

if TYPE_CHECKING:
    from research_agent.citations import CitationManager
    from research_agent.llm import GraniteClient, _WatsonxError
    from research_agent.summarizer import PaperSummary


# ---------------------------------------------------------------------------
# Report data model
# ---------------------------------------------------------------------------

class ResearchReport:
    """Holds all sections of a generated research report."""

    def __init__(self, topic: str) -> None:
        self.topic = topic
        self.generated_at = datetime.now().strftime("%Y-%m-%d %H:%M")
        self.introduction: str = ""
        self.literature_review: str = ""
        self.methodology_overview: str = ""
        self.key_findings: str = ""
        self.hypotheses: str = ""
        self.conclusion: str = ""
        self.references: str = ""

    # ------------------------------------------------------------------
    # Serialisation helpers
    # ------------------------------------------------------------------

    def to_markdown(self) -> str:
        sections = [
            f"# Research Report: {self.topic}",
            f"*Generated: {self.generated_at} | Powered by IBM Granite (watsonx.ai)*",
            "",
            "---",
            "",
            "## 1. Introduction",
            self.introduction,
            "",
            "## 2. Literature Review",
            self.literature_review,
            "",
            "## 3. Methodology Overview",
            self.methodology_overview,
            "",
            "## 4. Key Findings",
            self.key_findings,
            "",
            "## 5. Suggested Hypotheses",
            self.hypotheses,
            "",
            "## 6. Conclusion",
            self.conclusion,
            "",
            "## References",
            self.references,
        ]
        return "\n".join(sections)

    def to_text(self) -> str:
        """Plain-text version (80-char wrapped)."""
        md = self.to_markdown()
        # Strip markdown symbols for plain text
        import re
        text = re.sub(r"^#{1,6}\s+", "", md, flags=re.MULTILINE)
        text = re.sub(r"\*{1,2}(.+?)\*{1,2}", r"\1", text)
        text = re.sub(r"---+", "─" * 60, text)
        return text


# ---------------------------------------------------------------------------
# PDF builder
# ---------------------------------------------------------------------------

class _ReportPDF(FPDF):
    """Custom FPDF subclass for report layout."""

    TITLE_COLOR = (30, 80, 180)
    HEADING_COLOR = (50, 50, 50)
    BODY_COLOR = (30, 30, 30)
    ACCENT_COLOR = (100, 100, 200)

    def header(self) -> None:
        self.set_font("Helvetica", "B", 10)
        self.set_text_color(*self.ACCENT_COLOR)
        self.cell(0, 8, "IBM Granite Research Agent", align="R")
        self.ln(4)
        self.set_draw_color(200, 200, 200)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(3)

    def footer(self) -> None:
        self.set_y(-12)
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(150, 150, 150)
        self.cell(0, 8, f"Page {self.page_no()} | Generated by IBM Granite (watsonx.ai)", align="C")

    def chapter_title(self, title: str) -> None:
        self.set_font("Helvetica", "B", 13)
        self.set_text_color(*self.TITLE_COLOR)
        self.ln(4)
        self.cell(0, 9, title, ln=True)
        self.set_draw_color(*self.TITLE_COLOR)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(3)
        self.set_text_color(*self.BODY_COLOR)

    def body_text(self, text: str) -> None:
        self.set_font("Helvetica", "", 10)
        self.set_text_color(*self.BODY_COLOR)
        self.multi_cell(0, 5.5, text)
        self.ln(2)


